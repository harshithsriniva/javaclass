package controller;

public class mathfunctions {
    public static void main(String argc[]){
        int absValue = Math.abs(-1);
        System.out.println("Absolute Value = " +absValue);

        double squarRoot = Math.sqrt(25);
        System.out.println("Square Value = " +squarRoot);

        double powerValue = Math.pow(2,3);
        System.out.println("Power Value = " +powerValue);

        double sineValue = Math.sin(Math.PI/2);
        System.out.println("Sine Value = " +sineValue);
//        log, min, max, floor, ceil, round
    }
}
